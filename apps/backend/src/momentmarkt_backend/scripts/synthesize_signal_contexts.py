def synthesize_one(
    catalog_entry: dict[str, Any],
    city_meta: dict[str, Any],
    weather_trigger: str,
) -> dict[str, Any]:
    merchant_id = catalog_entry["id"]
    category = _normalize_category(catalog_entry["category"])
    distance_m = int(catalog_entry["distance_m"])

    # Synthesize a plausible lat/lon from Mia's center + bearing seed.
    bearing = _seed(merchant_id, "bearing") % 360
    lat, lon = _project_lat_lon(
        city_meta["center_lat"], city_meta["center_lon"], distance_m, bearing
    )

    typical_points = _typical_curve_points(category)
    live_samples = _live_samples(merchant_id, category, typical_points, city_meta["tz_offset"])
    demand_gap = _demand_gap(typical_points, live_samples, city_meta["tz_offset"])
    trigger_tags = _trigger_tags(category, weather_trigger, demand_gap["triggers_demand_gap"])

    return {
        "id": merchant_id,
        "display_name": catalog_entry["display_name"],
        "category": category,
        "canonical_demo_merchant": False,
        "synthesized": True,
        "location": {
            "label": f"{catalog_entry['display_name']}, {catalog_entry.get('neighborhood', '')}".strip(", "),
            "lat": lat,
            "lon": lon,
        },
        "distance_m": distance_m,
        "currency": city_meta["currency"],
        "timezone": city_meta["timezone"],
        "opening_hours": _opening_hours(category),
        "demo_window": {
            "start_local": f"{DEMO_DATE}T12:00:00{city_meta['tz_offset']}",
            "end_local": f"{DEMO_DATE}T15:00:00{city_meta['tz_offset']}",
        },
        "typical_density_curve": {
            "day_of_week": "saturday",
            "time_zone": city_meta["timezone"],
            "points": typical_points,
        },
        "live_samples": live_samples,
        "demand_gap": demand_gap,
        "trigger_tags": trigger_tags,
        "merchant_goal": CATEGORY_TEMPLATES[category]["merchant_goal"],
        "inventory_goal": _inventory_goal(merchant_id, category, city_meta["tz_offset"]),
        "offer_budget": _offer_budget(merchant_id, category),
        "autopilot_rule_hints": _autopilot_rule_hints(merchant_id, category, distance_m),
    }


def _existing_canonical_ids(city: str) -> set[str]:
    """Ids already pinned in `<city>-density.json`.

    Returns an empty set if the city has no canonical fixture (Zurich today).
    """
    candidate = DATA_DIR / "transactions" / f"{city}-density.json"
    if not candidate.exists():
        return set()
    with candidate.open("r", encoding="utf-8") as f:
        data = json.load(f)
    return {m["id"] for m in data.get("merchants", [])}


def synthesize_city(city: str) -> dict[str, Any]:
    if city not in CITY_META:
        raise SystemExit(f"Unknown city: {city}. Known: {sorted(CITY_META)}")
    meta = CITY_META[city]
    catalog = search_merchants(city, query=None, limit=200) or []
    canonical_ids = _existing_canonical_ids(city)
    weather_trigger = meta["demo_context"]["weather_trigger"]

    synthesized: list[dict[str, Any]] = []
    for entry in catalog:
        if entry["id"] in canonical_ids:
            continue
        synthesized.append(synthesize_one(entry, meta, weather_trigger))

    return {
        "fixture_id": meta["fixture_id"],
        "description": (
            "Synthesized signal-context layer for OSM-hydrated merchants in "
            f"{meta['city']}. Generated by "
            "momentmarkt_backend.scripts.synthesize_signal_contexts."
        ),
        "city": meta["city"],
        "currency": meta["currency"],
        "timezone": meta["timezone"],
        "density_unit": "index_0_100",
        "sample_granularity_minutes": 15,
        "demo_context": meta["demo_context"],
        "merchants": synthesized,
    }


# ---------------------------------------------------------------------------
# CLI entry-point.
# ---------------------------------------------------------------------------

def _output_path(city: str) -> Path:
    return DATA_DIR / "transactions" / f"{city}-density-synthesized.json"


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        description=(
            "Synthesize per-merchant signal-context entries for OSM-hydrated "
            "catalogs (write `data/transactions/<city>-density-synthesized.json`)."
        ),
    )
    parser.add_argument("city", choices=sorted(CITY_META.keys()))
    parser.add_argument(
        "--out",
        type=Path,
        default=None,
        help="Override output path (default: data/transactions/<city>-density-synthesized.json).",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Print summary without writing the file.",
    )
    args = parser.parse_args(argv)

    payload = synthesize_city(args.city)
    out_path = args.out or _output_path(args.city)

    fired = sum(
        1 for m in payload["merchants"] if m["demand_gap"]["triggers_demand_gap"]
    )
    auto = sum(1 for m in payload["merchants"] if m["autopilot_rule_hints"]["approved"])
    total = len(payload["merchants"])

    print(
        f"[synthesize_signal_contexts] city={args.city} synthesized={total} "
        f"demand_gap_fires={fired} ({(fired / total * 100) if total else 0:.0f}%) "
        f"autopilot_approved={auto}",
        file=sys.stderr,
    )

    if args.dry_run:
        return 0

    out_path.parent.mkdir(parents=True, exist_ok=True)
    with out_path.open("w", encoding="utf-8") as f:
        json.dump(payload, f, indent=2, ensure_ascii=False)
        f.write("\n")
    print(f"[synthesize_signal_contexts] wrote {out_path}", file=sys.stderr)
    # Touch load_density cache invalidation note (we don't actually clear it
    # — callers read from disk on next process start).
    _ = load_density  # silence unused-import warning if path lookup changes.
    return 0


if __name__ == "__main__":  # pragma: no cover
    raise SystemExit(main())

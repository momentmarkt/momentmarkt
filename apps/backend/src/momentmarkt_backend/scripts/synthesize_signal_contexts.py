"""Synthesize signal-context entries for OSM-hydrated merchants.

The canonical density fixture (`data/transactions/{city}-density.json`) only
covers a handful of hand-authored merchants per city (Berlin: 4 canonical;
Zurich: 0). The wallet drawer however lists ~35-40 OSM merchants per city
(see `momentmarkt_backend.merchants`). When `/demo/seed` runs, the LLM batch
pipeline asks `signals.build_signal_context(city, merchant_id)` for every
merchant. Without a density-fixture entry the call raises and the merchant
is skipped with `"no signal context"`.

This script generates a per-city `data/transactions/{city}-density-synthesized.json`
side-car with one entry per OSM merchant **not already in the canonical
fixture**. The shape is deliberately byte-identical to the canonical entries
so `signals.build_signal_context` does not need to branch.

Determinism: every per-merchant draw is seeded by `hash(merchant_id) % N`.
That keeps re-runs reproducible and ensures roughly half the merchants fire
the demand-gap trigger at 13:30 (the demo moment) while the other half do
not — gives the merchant inbox a believable "Vorgeschlagen" tab population.

CLI:
    python -m momentmarkt_backend.scripts.synthesize_signal_contexts berlin
    python -m momentmarkt_backend.scripts.synthesize_signal_contexts zurich
"""


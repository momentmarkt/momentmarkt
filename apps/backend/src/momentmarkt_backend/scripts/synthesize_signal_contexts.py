# ---------------------------------------------------------------------------
# Determinism helpers.
# ---------------------------------------------------------------------------

def _seed(merchant_id: str, salt: str = "") -> int:
    """Return a deterministic non-negative int for `merchant_id` + optional salt.

    Python's built-in `hash()` is salted between processes (PYTHONHASHSEED),
    so we use a stable algorithm here. djb2-style is enough for our needs.
    """
    h = 5381
    for ch in (merchant_id + "::" + salt):
        h = ((h << 5) + h + ord(ch)) & 0xFFFFFFFF
    return h


def _seeded_pick(merchant_id: str, salt: str, lo: int, hi: int) -> int:
    """Deterministic int in [lo, hi] inclusive."""
    span = hi - lo + 1
    return lo + (_seed(merchant_id, salt) % span)


def _seeded_unit(merchant_id: str, salt: str) -> float:
    """Deterministic float in [0, 1)."""
    return (_seed(merchant_id, salt) % 10000) / 10000.0


# ---------------------------------------------------------------------------
# Geometry helpers.
# ---------------------------------------------------------------------------

def _haversine_m(lat1: float, lon1: float, lat2: float, lon2: float) -> int:
    earth_radius_m = 6_371_000
    phi1 = radians(lat1)
    phi2 = radians(lat2)
    d_phi = radians(lat2 - lat1)
    d_lambda = radians(lon2 - lon1)
    a = sin(d_phi / 2) ** 2 + cos(phi1) * cos(phi2) * sin(d_lambda / 2) ** 2
    return round(2 * earth_radius_m * asin(sqrt(a)))


def _project_lat_lon(
    center_lat: float,
    center_lon: float,
    distance_m: int,
    bearing_deg: float,
) -> tuple[float, float]:
    """Move `distance_m` from (center_lat, center_lon) along `bearing_deg`."""
    R = 6_371_000.0
    bearing = radians(bearing_deg)
    phi1 = radians(center_lat)
    lam1 = radians(center_lon)
    d_over_r = distance_m / R

    phi2 = asin(sin(phi1) * cos(d_over_r) + cos(phi1) * sin(d_over_r) * cos(bearing))
    lam2 = lam1 + atan2(
        sin(bearing) * sin(d_over_r) * cos(phi1),
        cos(d_over_r) - sin(phi1) * sin(phi2),
    )
    return round(degrees(phi2), 6), round(degrees(lam2), 6)


